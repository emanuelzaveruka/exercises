package com.emanuel.aula029;
//laço for
public class Aula029{
	
	public static void main(String[]args) {
		
		for(int i = 0; i <= 10; i ++) {
			System.out.println(i);
		}
		
		/*
		 * for (parte1; parte2; parte3)
		 * 
		 * parte1: é aonde nós declaramos uma variavel
		 * 
		 * parte2: é aonde nós colocamos uma condição
		 * para que o loopinh continue ou seja terminado
		 * 
		 * parte3: é aonde nós incrementamos a nossa variavel
		 * 
		 */
	}
	
}
